package yl.bigdata.doristask.entity;

public class Doris {
    private String date_time;
    private int island;
    private int districtprescription;
    private int is_pres_sign;
    private int is_real_sign;


    public String getDate_time() {
        return date_time;
    }

    public void setDate_time(String date_time) {
        this.date_time = date_time;
    }


    public int getIsland() {
        return island;
    }

    public void setIsland(int island) {
        this.island = island;
    }

    public int getDistrictprescription() {
        return districtprescription;
    }

    public void setDistrictprescription(int districtprescription) {
        this.districtprescription = districtprescription;
    }

    public int getIs_pres_sign() {
        return is_pres_sign;
    }

    public void setIs_pres_sign(int is_pres_sign) {
        this.is_pres_sign = is_pres_sign;
    }

    public int getIs_real_sign() {
        return is_real_sign;
    }

    public void setIs_real_sign(int is_real_sign) {
        this.is_real_sign = is_real_sign;
    }
}
